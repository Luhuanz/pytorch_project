class FReLU(nn.Module):
    # FReLU activation https://arxiv.org/abs/2007.11824
    def __init__(self, c1, k=3):  # ch_in, kernel
        super().__init__()
        self.conv = nn.Conv2d(c1, c1, k, 1, 1, groups=c1, bias=False)
        self.bn = nn.BatchNorm2d(c1)

    def forward(self, x):
        return torch.max(x, self.bn(self.conv(x)))


class AconC(nn.Module):
    r""" ACON activation (activate or not)
    AconC: (p1*x-p2*x) * sigmoid(beta*(p1*x-p2*x)) + p2*x, beta is a learnable parameter
    according to "Activate or Not: Learning Customized Activation" <https://arxiv.org/pdf/2009.04759.pdf>.
    """
#PyTorchçš„è‡ªå®šä¹‰æ¿€æ´»å‡½æ•°ï¼Œå«åšACONï¼ˆActivate or Notï¼‰ï¼Œ
# å®ƒçš„ä¸»è¦æ€æƒ³æ˜¯åœ¨æ¿€æ´»å‡½æ•°ä¸­å¢åŠ ä¸€ä¸ªå¯å­¦ä¹ çš„å‚æ•°betaï¼Œä»¥å®ç°æ›´å¥½çš„éçº¿æ€§å˜æ¢ï¼Œä»è€Œæé«˜æ¨¡å‹çš„æ€§èƒ½ã€‚
#ACONæ¿€æ´»å‡½æ•°çš„æ•°å­¦å…¬å¼ä¸º(p1x-p2x) * sigmoid(beta*(p1x-p2x)) + p2*xï¼Œå…¶ä¸­xä¸ºè¾“å…¥ï¼Œp1ã€p2å’Œbetaä¸ºå¯å­¦ä¹ çš„å‚æ•°ã€‚
    def __init__(self, c1):
        super().__init__()
        self.p1 = nn.Parameter(torch.randn(1, c1, 1, 1))
        self.p2 = nn.Parameter(torch.randn(1, c1, 1, 1))
        self.beta = nn.Parameter(torch.ones(1, c1, 1, 1))

    def forward(self, x):
        dpx = (self.p1 - self.p2) * x
        return dpx * torch.sigmoid(self.beta * dpx) + self.p2 * x


class MetaAconC(nn.Module):
    r""" ACON activation (activate or not)
    MetaAconC: (p1*x-p2*x) * sigmoid(beta*(p1*x-p2*x)) + p2*x, beta is generated by a small network
    according to "Activate or Not: Learning Customized Activation" <https://arxiv.org/pdf/2009.04759.pdf>.
    """
#MetaAconC æ˜¯åŸºäº ACON æ¿€æ´»å‡½æ•°çš„ä¸€ç§å˜ç§ã€‚ä¸ AconC ä¸åŒçš„æ˜¯ï¼ŒMetaAconC ä¸­çš„å‚æ•° beta æ˜¯ç”±ä¸€ä¸ªå°å‹ç½‘ç»œç”Ÿæˆçš„
#å…·ä½“æ¥è¯´ï¼ŒMetaAconC ä¸­æœ‰ä¸¤ä¸ªå·ç§¯å±‚å’Œä¸¤ä¸ªå¯å­¦ä¹ çš„åç½®å‚æ•° p1 å’Œ p2ï¼Œå…¶ä¸­ç¬¬ä¸€ä¸ªå·ç§¯å±‚å°†è¾“å…¥çš„ç‰¹å¾å›¾ç»è¿‡ä¸€å®šçš„å˜æ¢å¾—åˆ°ä¸€ä¸ªä¸­é—´ç‰¹å¾å›¾ï¼Œ
# ç„¶åç¬¬äºŒä¸ªå·ç§¯å±‚å°†ä¸­é—´ç‰¹å¾å›¾å˜æ¢ä¸º beta å‚æ•°ï¼Œæœ€åé€šè¿‡ ACON å‡½æ•°æ¥è®¡ç®—æ¿€æ´»åçš„è¾“å‡ºã€‚
# ç›¸å¯¹äº AconCï¼ŒMetaAconC åœ¨è®¡ç®— beta å‚æ•°æ—¶å¼•å…¥äº†ä¸€å®šçš„ç½‘ç»œç»“æ„ï¼Œå¯ä»¥æ›´åŠ çµæ´»åœ°é€‚åº”å„ç§è¾“å…¥ã€‚
    def __init__(self, c1, k=1, s=1, r=16):  # ch_in, kernel, stride, r
        super().__init__()
        c2 = max(r, c1 // r)
        self.p1 = nn.Parameter(torch.randn(1, c1, 1, 1))
        self.p2 = nn.Parameter(torch.randn(1, c1, 1, 1))
        self.fc1 = nn.Conv2d(c1, c2, k, s, bias=True)
        self.fc2 = nn.Conv2d(c2, c1, k, s, bias=True)
        # self.bn1 = nn.BatchNorm2d(c2)
        # self.bn2 = nn.BatchNorm2d(c1)

    def forward(self, x):
        y = x.mean(dim=2, keepdims=True).mean(dim=3, keepdims=True)
        # batch-size 1 bug/instabilities https://github.com/ultralytics/yolov5/issues/2891
        # beta = torch.sigmoid(self.bn2(self.fc2(self.bn1(self.fc1(y)))))  # bug/unstable
        beta = torch.sigmoid(self.fc2(self.fc1(y)))  # bug patch BN layers removed
        dpx = (self.p1 - self.p2) * x
        return dpx * torch.sigmoid(beta * dpx) + self.p2 * x
